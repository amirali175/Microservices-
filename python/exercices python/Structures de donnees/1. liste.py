# la fonction append (permet ajoute des elements dans une liste )

liste = [1,2,3,4,5] # je predifinies ma liste 

liste.append(6) # j'ajoute ma liste un element 

print(liste) # permet d'afficher les result de ma liste

# La fonction Len (retourne la taille nombre de la liste )

print(len(liste))


# La fonction extend permet agrandir une liste ajouter aussi elements extrait 

liste.extend([4,6])

print (liste)


#la fonction remove sert a enlever l'element de la liste que nous voulons 

liste.remove(3)

print(liste)





